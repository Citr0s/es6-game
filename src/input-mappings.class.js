const moveLeft = "A";
const moveRight = "D";
const moveUp = "W";
const moveDown = "S";

class InputMappings {
    static get MOVE_LEFT() {
        return moveLeft;
    }

    static get MOVE_RIGHT() {
        return moveRight;
    }

    static get MOVE_UP() {
        return moveUp;
    }

    static get MOVE_DOWN() {
        return moveDown;
    }
}

export default InputMappings;