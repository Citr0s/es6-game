const moveLeft = "A";
const moveRight = "D";
const moveUp = "W";
const moveDown = "S";

class InputMappings {
    static MOVE_LEFT() {
        return moveLeft;
    }

    static MOVE_RIGHT() {
        return moveRight;
    }

    static MOVE_UP() {
        return moveUp;
    }

    static MOVE_DOWN() {
        return moveDown;
    }
}